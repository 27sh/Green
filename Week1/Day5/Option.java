package day01;

public class Option {
	
	public int n1;
	public int n2;
	
	public int add (int n1, int n2) {
		return n1 + n2;
	}
	
	public int dev (int n1, int n2) {
		return n1 - n2;
	}
	
	public int mul (int n1, int n2) {
		return n1 * n2;
	}
	
	public float div (float n1, float n2) {
		return n1 / n2;
	}
}
